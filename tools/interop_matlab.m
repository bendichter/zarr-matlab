function interop_matlab(pyStore, mlStore)
%INTEROP_MATLAB Step 2: verify the python-written store, then write the
%   mirror store for python to verify.

cases = interop_case_list();

% ---- verify python-written store -------------------------------------
root = zarr.open(pyStore);
assert(root.attrs.title == "interop" && root.attrs.answer == 42, 'root attrs');
sub = root.item("sub");
assert(sub.attrs.depth == 1, 'sub attrs');

for i = 1:numel(cases)
    c = cases{i};
    z = zarr.open(pyStore, Path=c.name);
    expected = interop_pattern(c.shape, c.dtype);
    if ~isempty(c.partial)
        % Only a corner region was written; the rest must be fill.
        full = repmat(z.meta.fillValue, zarr.internal.mshape(c.shape));
        subs = arrayfun(@(p) 1:p, c.partial, 'UniformOutput', false);
        if isscalar(subs), subs{end + 1} = 1; end
        full(subs{:}) = expected(subs{:});
        expected = full;
    end
    if isempty(c.shape)
        actual = z();
    else
        actual = z.read();
    end
    assert(isequaln(actual, expected), 'read mismatch: %s', c.name);
    assert(isequal(class(actual), class(expected)), 'class mismatch: %s', c.name);
end

named = zarr.open(pyStore, Path="sub/named");
assert(isequaln(named.read(), interop_pattern([4 6], "float64")), 'named data');
assert(isequal(named.dimensionNames, ["y" "x"]), 'dimension_names');
assert(named.attrs.units == "mm" && named.attrs.scale == 1.5, 'array attrs');
fprintf('MATLAB verified %d python-written arrays\n', numel(cases) + 1);

% ---- write the mirror store for python to verify ----------------------
zarr.create_group(mlStore, Attributes=struct('title', 'interop', 'answer', 42));
g = zarr.open(mlStore);
g.createGroup("sub").setAttr('depth', 1);

nWritten = 0;
for i = 1:numel(cases)
    c = cases{i};
    if c.writeSkip
        continue
    end
    extra = {};
    if ~isempty(c.shards)
        extra = {'ShardShape', c.shards, 'IndexLocation', c.indexLocation};
    end
    if c.v2keys
        extra = [extra, {'ChunkKeyEncoding', "v2"}];
    end
    z = zarr.create(mlStore, c.shape, c.dtype, 'Path', c.name, ...
        'ChunkShape', c.chunks, 'Codecs', c.codecs, 'FillValue', c.fill, extra{:});
    data = interop_pattern(c.shape, c.dtype);
    if ~isempty(c.partial)
        subs = arrayfun(@(p) 1:p, c.partial, 'UniformOutput', false);
        if isscalar(subs), subs{end + 1} = 1; end
        z.write(data(subs{:}), ones(1, numel(c.shape)));
    else
        z.write(data);
    end
    nWritten = nWritten + 1;
end

named = zarr.create(mlStore, [4 6], "float64", Path="sub/named", ChunkShape=[2 3], ...
    DimensionNames=["y" "x"], Attributes=struct('units', 'mm', 'scale', 1.5));
named.write(interop_pattern([4 6], "float64"));
fprintf('MATLAB wrote %d arrays\n', nWritten + 1);
end

