function c = cs(name, dtype, shape, chunks, codecs, fill, varargin)
c = struct('name', name, 'dtype', dtype, 'shape', shape, ...
    'chunks', chunks, 'codecs', {codecs}, 'fill', fill, ...
    'partial', [], 'writeSkip', false, 'shards', [], ...
    'indexLocation', "end", 'v2keys', false);
for i = 1:2:numel(varargin)
    c.(varargin{i}) = varargin{i + 1};
end
end
